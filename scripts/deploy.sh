#!/usr/bin/env bash
# ЕДИНСТВЕННЫЙ способ выкатывать мост. Не запускать cp/kickstart руками.
#
# Зачем скрипт: 18.07 я дважды убил живую стройку Анвара, перезапуская мост.
# После первого раза сделал проверку — и во второй раз ВЫКАТИЛ ВОПРЕКИ ЕЁ ВЕРДИКТУ,
# потому что запускал проверку и деплой отдельными командами: она печатала «НЕ ДЕПЛОИТЬ»,
# а следующая строка всё равно копировала файлы и перезапускала сервис.
# Проверка бесполезна, если её результат никто не обязан соблюдать. Здесь — обязан:
# preflight падает → скрипт выходит, ничего не тронув.
set -euo pipefail
cd "$(dirname "$0")/.."

echo "── ПРЕДПОЛЁТНАЯ ПРОВЕРКА ──"
if ! ./scripts/preflight_ui.sh; then
  echo
  echo "ДЕПЛОЙ ОТМЕНЁН: проверка не пройдена. Ничего не скопировано, мост не тронут."
  exit 1
fi

echo
echo "── ДЕПЛОЙ ──"
cp ui/*.py ui/wizard.html ~/extella_wizard/app/
echo "   файлы скопированы"
launchctl kickstart -k gui/501/ai.extella.wizard-bridge
sleep 3
V=$(curl -s --max-time 10 http://127.0.0.1:8765/x/health | python3 -c 'import sys,json;print(json.load(sys.stdin).get("version","?"))' 2>/dev/null || echo "нет ответа")
echo "   мост поднялся, версия: $V"
